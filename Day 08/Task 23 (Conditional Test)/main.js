//Task 23 (Conditional Tests)
//**Making a Veriable**
var five = 5;
var ten = 10;
//Test 1 Eqaulity Comparison (TRUE)
console.log("\nTest 1: five is equal to 5?");
console.log(five == 5); //true
//Test 2 Equality Comparison (TRUE) 
console.log("\nTest 2: ten is equal to 10?");
console.log(ten == 10); //true
//Test 3 INeqaulity comparison (TRUE)
console.log("\nTest 3: I predict five is not eqaul ten [TRUE]");
console.log(five != ten); //true
//Test 4 Greater Than comparison (TRUE)
console.log("\nTest 4: I predict 10 is greater then 5[TRUE]");
console.log(ten > five); //true
//Test 5 Less Than comparison (TRUE)
console.log("\nTest 5: I predict 5 is smaller then 10[TRUE]");
console.log(five < ten); //true
//Reverse Method for Answer False
//Test 1 Eqaulity Comparison (False)
console.log("\nTest 1: five is equal to 10?");
console.log(five == 10); //false
//Test 2 Equality Comparison (False) 
console.log("\nTest 2: ten is equal to 5?");
console.log(ten == 5); //false
//Test 3 INeqaulity comparison (False)
console.log("\nTest 3: I predict five is not eqaul to 5 [False]");
console.log(five != 5); //false
//Test 4 Greater Than comparison (TRUE)
console.log("\nTest 4: I predict 5 is greater then 10[FALSE]");
console.log(five > 10); //false
//Test 5 Less Than comparison (False)
console.log("\nTest 5: I predict 10 is smaller then 5[FALSE]");
console.log(10 < 5); //false
